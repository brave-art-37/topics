void solve(){
  int n;cin>>n;
  vector<long long> nos(n,0);
  for(long long &x:nos){
    cin>>x;
  }
  long long s1=0,s3=0,ans=0;
  int j=n-1;
  for(int i=0;i<n;i++){
    s1+=nos[i];
    while(s3<s1 && j>-1){
      s3+=nos[j];j--;
    }
    if(j<i) break;
    else if(s1==s3) ans=max(ans,s1);
  }
  cout<<ans<<"\n";
}

